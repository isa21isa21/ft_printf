#include "ft_printf.h"

void		ft_putuint(unsigned long int n)
{
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
		n = n - 4294967294;
	}
	if (n >= 10)
		ft_putnbr(n / 10);
	ft_putchar(n % 10 + '0');
}
